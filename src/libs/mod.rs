//! # 核心业务逻辑库
//!
//! 该模块包含应用程序的核心业务逻辑，提供数据获取、存储、处理和导出功能。
//! 这些模块是应用程序的业务核心，独立于用户界面实现。
//!
//! ## 模块组织
//!
//! - export_csv: CSV数据导出功能
//! - fetcher: 从XJTU服务器获取交易数据
//! - transactions: 交易数据管理和数据库操作
//!
//! ## 数据流程
//!
//! ```text
//! ┌─────────────┐    ┌─────────────┐    ┌─────────────┐
//! │   Fetcher   │───▶│ Transactions│───▶│ Export CSV  │
//! │ 获取原始数据  │    │  数据管理     │    │  导出数据     │
//! └─────────────┘    └─────────────┘    └─────────────┘
//!        │                   │                   │
//!        ▼                   ▼                   ▼
//!   XJTU服务器           SQLite数据库         CSV文件
//! ```
//!
//! ## 主要特性
//!
//! - **数据获取**: 支持真实和模拟数据源
//! - **数据存储**: 使用SQLite进行本地缓存
//! - **数据筛选**: 支持按时间、商家、金额筛选
//! - **数据导出**: 支持CSV格式导出
//! - **并发安全**: 支持多线程访问
//! - **错误处理**: 完善的错误处理和恢复机制

/// CSV数据导出功能模块
///
/// 提供将交易记录导出为CSV格式的功能，支持多种筛选条件和导出选项。
pub mod export_csv;

/// 数据获取模块
///
/// 负责从XJTU校园卡服务器获取交易记录，支持真实数据和模拟数据两种模式。
pub mod fetcher;

/// 交易数据管理模块
///
/// 提供交易记录的数据库操作、筛选查询和数据管理功能。
pub mod transactions;
